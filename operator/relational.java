package operator;

public class relational {
    public static void main(String[] args) {
        int a=2,b=5,c=7;
        if(a==2)
            System.out.println("A Equal 2");
        if(a!=5)
            System.out.println("A Not Equal 5");
        if(c>a)
            System.out.println("C Greater than A");
        if(b<c)
            System.out.println("B Less than C");
        if(b>=a)
            System.out.println("B Greater than or Equal A");
        if(a<=b)
            System.out.println("A Less than or Equal B");
    }
}